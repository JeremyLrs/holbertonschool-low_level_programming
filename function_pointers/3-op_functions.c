#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - addition
 * @a: num 1
 * @b: num 2
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
* op_add - subbstraction
 * @a: num 1
 * @b: num 2
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_add - multiplication
 * @a: num 1
 * @b: num 2
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_add - division
 * @a: num 1
 * @b: num 2
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
    return (a / b);
}

/**
* op_add - modulo
 * @a: num 1
 * @b: num 2
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
    return (a % b);
}